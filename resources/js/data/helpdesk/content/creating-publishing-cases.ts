const content = `# Creating and Publishing Cases

Case creation captures the official intake record for an OFW or family assistance concern. A published case receives **CASE-YYYYMMDD-XXXX** and public tracker **OWBAP-XXXXXXX**. Drafts remain owner-only until published.

![Case create top](/assets/helpdesk/cases-create-top.png)

## Intake sections

### Client information

Record client type, name, contact details, and vulnerability indicator when relevant. Contact details matter because public tracking uses OTP delivery.

![Client information](/assets/helpdesk/cases-create-client.png)

### Address and employment

Enter address details consistently. Employment information helps determine category, issue, and referral service. If unknown, save a draft rather than guessing.

![Address section](/assets/helpdesk/cases-create-address.png)

### Next of kin

The form supports multiple next-of-kin entries. Add complete names, relationships, contact details, and vulnerability indicators when available.

![Next-of-kin section](/assets/helpdesk/cases-create-nok.png)

### Consent, category, issue, and summary

Capture consent before processing. Select the best category and case issue. Write a factual summary explaining the problem, prior actions, and needed assistance.

## Save draft or publish

Use **DRAFT** when information is incomplete. Drafts can be edited, deleted, or published later. Publish when ready for official handling; the case becomes **OPEN**.

![Draft cases](/assets/helpdesk/cases-drafts.png)
![Cases index](/assets/helpdesk/cases-index.png)

## After publishing

Open the case details page, confirm the case number and tracker number, review the summary, create referrals if needed, and explain OTP-based tracking to the client.

![Case details](/assets/helpdesk/cases-show.png)
`;
